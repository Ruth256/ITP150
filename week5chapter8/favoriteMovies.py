#8-2. Favorite Movie: Write a function called favorite_movie() that accepts one parameter, title. The function should print a message, such as One of my favorite movies is Alice in Wonderland. Call the function, making sure to include a movie title as an argument in the function call.

def favorite_movies(title):
    """Function to display my favorite movies"""
    print(f"My favorite movie is {title}!")

favorite_movies("Titanic")
